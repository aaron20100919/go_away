def draw_random_walls():
    x = random.randrange(screen_size[0])
    y = random.randrange(screen_size[1])

    for _ in range(20):
        new_wall = Wall(x, y)
        while pygame.sprite.spritecollide(
            new_wall, walls, True, pygame.sprite.collide_rect
        ):
            new_wall = Wall(x, y)
        walls.add(new_wall)
        x = random.randrange(screen_size[0])
        y = random.randrange(screen_size[1])

    all_sprites.add(walls)


